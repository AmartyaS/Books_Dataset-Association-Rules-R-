install.packages("arulesViz")
library("arules")
library("psych")
library("arulesViz")
#Book Calculation
data <- read.csv(file.choose())
View(data)
rules <- apriori(as.matrix(data, parameter =list(support=2,minlen=2,confidence=0.7)))
summary(rules)
inspect(sort(rules, by="lift"))
plot(rules, pch=15)
plot(rules,jitter=4,interactive = TRUE)
